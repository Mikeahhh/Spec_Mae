def get_train_loader(
    train_dir:   str | Path,
    cfg:         Optional[AudioConfig] = None,
    batch_size:  int  = 32,
    num_workers: int  = 4,
    shuffle:     bool = True,
    pin_memory:  Optional[bool] = None,
) -> DataLoader:
    """DataLoader for the training split (normal samples only)."""
    cfg  = cfg or AudioConfig()
    ext  = LogMelExtractor(cfg=cfg)
    ds   = AudioDataset(train_dir, extractor=ext, label=0)
    return DataLoader(
        ds,
        batch_size=batch_size,
        shuffle=shuffle,
        num_workers=num_workers,
        pin_memory=pin_memory if pin_memory is not None else _auto_pin_memory(),
        drop_last=True,
    )


def get_val_loader(
    val_dir:     str | Path,
    cfg:         Optional[AudioConfig] = None,
    batch_size:  int = 64,
    num_workers: int = 4,
    pin_memory:  Optional[bool] = None,
) -> DataLoader:
    """DataLoader for the validation split (normal samples only)."""
    cfg  = cfg or AudioConfig()
    ext  = LogMelExtractor(cfg=cfg)
    ds   = AudioDataset(val_dir, extractor=ext, label=0)
    return DataLoader(
        ds,
        batch_size=batch_size,
        shuffle=False,
        num_workers=num_workers,
        pin_memory=pin_memory if pin_memory is not None else _auto_pin_memory(),
    )


def get_test_loader(
    normal_dir:  str | Path,
    anomaly_dir: str | Path,
    cfg:         Optional[AudioConfig] = None,
    batch_size:  int = 64,
    num_workers: int = 4,
    pin_memory:  Optional[bool] = None,
) -> DataLoader:
    """DataLoader for the test split (normal + anomaly, with labels)."""
    cfg = cfg or AudioConfig()
    ext = LogMelExtractor(cfg=cfg)
    ds  = AnomalyTestDataset(normal_dir, anomaly_dir, extractor=ext)
    return DataLoader(
        ds,
        batch_size=batch_size,
        shuffle=False,
        num_workers=num_workers,
        pin_memory=pin_memory if pin_memory is not None else _auto_pin_memory(),
    )


def make_kfold_loaders(
    all_files:   list[Path],
    fold_idx:    list[int],
    val_indices: list[int],
    cfg:         Optional[AudioConfig] = None,
    batch_size:  int = 32,
    num_workers: int = 4,
) -> tuple[DataLoader, DataLoader]:
    """
    Build train / val DataLoaders for one K-fold split.

    Args:
        all_files:   Complete list of file paths in the training set.
        fold_idx:    Indices (into all_files) used for training.
        val_indices: Indices (into all_files) used for validation.
        cfg:         AudioConfig.
        batch_size:  Mini-batch size.
        num_workers: Worker processes.

    Returns:
        (train_loader, val_loader)
    """
    cfg = cfg or AudioConfig()
    ext = LogMelExtractor(cfg=cfg)

    class _FileListDataset(Dataset):
        def __init__(self, paths: list[Path]) -> None:
            self.paths = paths
        def __len__(self) -> int:
            return len(self.paths)
        def __getitem__(self, i: int) -> tuple[torch.Tensor, int]:
            return ext(str(self.paths[i])), 0   # label=0 (all normal)

    train_ds = _FileListDataset([all_files[i] for i in fold_idx])
    val_ds   = _FileListDataset([all_files[i] for i in val_indices])

    _pin = _auto_pin_memory()
    train_loader = DataLoader(
        train_ds, batch_size=batch_size, shuffle=True,
        num_workers=num_workers, pin_memory=_pin, drop_last=True,
    )
    val_loader = DataLoader(
        val_ds, batch_size=batch_size, shuffle=False,
        num_workers=num_workers, pin_memory=_pin,
    )
    return train_loader, val_loader
